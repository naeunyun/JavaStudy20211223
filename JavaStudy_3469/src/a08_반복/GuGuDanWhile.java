package a08_반복;

public class GuGuDanWhile {

	public static void main(String[] args) {
		/*
		 * 구구단 2단 출력
		 * 2 x 1 = 2
		 * 2 x 2 = 4
		 * 2 x 3 = 6
		 * ...
		 * 2 x 9 = 18
		 * 
		 * 알고리즘은 문제를 해결할 수 있는 능력(공식)
		 * 규칙을 찾아야한다 그것을 소스코드로 구현해야함
		 * 
		 */
		
		int num = 1;
			
		while(num < 10) {
			System.out.println("2 x " + num + " = " + (2*num));
			num++;
			
		
			}
		}
		
		
	}

